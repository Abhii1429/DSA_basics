package Conditionals;
public class relationalOperator {
    public static void main(String[] args) {
        //1st relational Op(RO)-Equality Operator:
        int a =5;
        int b = 3;
        int c = 5;
        System.out.println(a == b);
        System.out.println(a == c);
        System.out.println(a != b);//2nd RO
        System.out.println(a > b);  //3rd ro
        System.out.println(a < b); //4th ro
        System.out.println(a >= b); //5th
        System.out.println(a <= b);//6th
    }
}
