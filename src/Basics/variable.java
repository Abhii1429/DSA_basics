package Basics;

public class variable {
    public static void main(String[] args) {
        int x;//Declaration of variable
        x = 6;//Initialisation of var.
        System.out.println(x);//this 'x' prints variable and a var can only be printed iff it's initialised earlier.

        System.out.println("x");// anything under double quotes is treated as a string or character and printed exactly the same.

        //next method of declaration & initialisation

        int y = 8;//Initialization & Declaration
        System.out.println(x+y);
    }
}
